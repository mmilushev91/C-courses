#include <stdio.h>

int main()
{
    printf("My average grade is: %d\n", 93.7);// Return strange number
    printf("My average grade is: %.1f\n", 93.7); //%f is used for floating point numbers
                                                // the number before f tells how many decimal places we want to be shown
                                                //we have to put. before the number
    return 0;
}
