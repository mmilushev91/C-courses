int main()
{
    int day, month, year;
    
    scanf("Enter day: ");
    scanf("%d", &day);
    
    scanf("Enter month: ");
    scanf("%d", &month);
    
    scanf("Enter year: ");
    scanf("%d", &year);
    
    
    printf("Character day hexadecimal value: %X\n", day);
    printf("Character month hexadecimal value: %X\n", month);
    printf("Character year hexadecimal value: %X\n", year);

    return 0;
}
